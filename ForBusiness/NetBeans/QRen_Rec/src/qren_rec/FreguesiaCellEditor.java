package fme;

import java.awt.Component;
import javax.swing.DefaultCellEditor;
import javax.swing.JComboBox;
import javax.swing.JTable;
















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































class FreguesiaCellEditor
  extends DefaultCellEditor
{
  FreguesiaCellEditor(JComboBox jc)
  {
    super(jc);
    setClickCountToStart(1);
  }
  
  void force_stopCellEditing() {
    super.stopCellEditing();
  }
  




  public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column)
  {
    Component c = super.getTableCellEditorComponent(table, value, isSelected, 
      row, column);
    
    if (table.getName().startsWith("PromLocal")) {
      CBData.PromLocal._filter_populate_freguesias((SteppedComboBox)c, row);
    }
    






    if (table.getName().equals("QInv_Tabela"))
      CBData.QInv._filter_populate_estabs((SteppedComboBox)c, row);
    if (table.getName().equals("QuadrosTecn_Tabela"))
      CBData.QuadrosTecn._filter_populate_estabs((SteppedComboBox)c, row);
    if (table.getName().equals("DesafiosSocietais_Tabela"))
      CBData.DesafiosSocietais._filter_populate_linhas((SteppedComboBox)c, row);
    if (table.getName().equals("DominiosPrioritarios_Tabela"))
      CBData.DominiosPrioritarios._filter_populate_areas((SteppedComboBox)c, row);
    if (table.getName().equals("DominiosPrioritarios_Tabela"))
      CBData.DominiosPrioritarios._filter_populate_areas((SteppedComboBox)c, row);
    if (table.getName().equals("DominiosPrioritariosCentro_Tabela"))
      CBData.DominiosPrioritariosCentro._filter_populate_areas((SteppedComboBox)c, row);
    if (table.getName().equals("DominiosPrioritariosLisboa_Tabela"))
      CBData.DominiosPrioritariosLisboa._filter_populate_areas((SteppedComboBox)c, row);
    if (table.getName().equals("DominiosPrioritariosAlgarve_Tabela")) {
      CBData.DominiosPrioritariosAlgarve._filter_populate_areas((SteppedComboBox)c, row);
    }
    return c;
  }
}
