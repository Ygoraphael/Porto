package fme;

import java.text.DecimalFormat;
import javax.swing.JOptionPane;














































































































































































































































































































































































































































































































































































































































class CFType_Int
  extends CFTypeNumber
{
  CFType_Int()
  {
    FMT = new DecimalFormat("#");
  }
  
  boolean setText(String _t, boolean interact) {
    boolean ok = super.setText(_t, interact);
    if (!ok) {
      return false;
    }
    
    ok = _lib.is_digitsOnly(_t);
    if (!ok) {
      if (interact) {
        JOptionPane.showMessageDialog(null, "Valor incorreto. " + _t, "Erro", 
          2);
      }
      return ok;
    }
    
    return ok;
  }
  
  boolean acceptKey(char c) {
    if (Character.isDigit(c)) {
      return true;
    }
    return false;
  }
}
