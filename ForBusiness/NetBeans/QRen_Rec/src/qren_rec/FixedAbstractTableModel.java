package fme;

import java.util.Vector;
import javax.swing.table.AbstractTableModel;













































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































class FixedAbstractTableModel
  extends AbstractTableModel
{
  Vector data;
  Object[] column;
  int i;
  
  FixedAbstractTableModel(Vector data, Object[] column, int i)
  {
    this.data = data;
    this.column = column;
    this.i = i;
  }
  
  public int getColumnCount() { return i; }
  
  public int getRowCount() {
    return data.size();
  }
  
  public String getColumnName(int col) { return (String)column[col]; }
  
  public Object getValueAt(int row, int col) {
    return ((String[])data.elementAt(row))[col];
  }
}
