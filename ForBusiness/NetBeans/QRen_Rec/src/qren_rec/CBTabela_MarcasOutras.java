class CBTabela_MarcasOutras
  extends CBTabela
{
  Frame_Concorrencia P12;
  
  public String getPagina()
  {
    return "Concorrencia";
  }
  

  int tab_index = 0;
  
  CBTabela_MarcasOutras() {
    P12 = ((Frame_Concorrencia)fmeApp.Paginas.getPage("Concorrencia"));
    if (P12 == null) return;
    initialize();
  }
  
  CBTabela_MarcasOutras(Frame_Concorrencia p, int idx) {
    tab_index = idx;
    P12 = p;
    initialize();
  }
  
  void initialize() {
    tag = "MarcasOutras";
    started = true;
    
    cols = new CHTabColModel[2];
    cols[0] = new CHTabColModel("marca", "Marca", true, true, true, null);
    cols[1] = new CHTabColModel("perc", "Quota de<br>mercado<br>(%)", true, true, true, CFLib.VLD_PERC_0);
    
    init_dados(5);
    

    init_handler(P12.getJTable_MarcasOutras());
    P12.getJTable_MarcasOutras().addKeyListener(new TableKeyListener(this));
    handler.width = P12.getJScrollPane_MarcasOutras().getWidth();
    
    handler.set_col_text(0, 0.755D, null);
    handler.set_col_text(1, 0.2D, "C");
  }
  
  CHValid_Grp validar(CHValid_Grp err_list, String cp)
  {
    String titulo = "Principais marcas da área de negócio";
    if (cp.length() > 0) { titulo = titulo + cp;
    }
    if (err_list == null)
      err_list = new CHValid_Grp(this, titulo);
    if (!started) { return err_list;
    }
    


    for (int i = 0; i < dados.size(); i++) {
      StringBuffer mask = new StringBuffer("RR");
      TabError[] e = isIncompletAll(i, mask.toString());
      for (int ii = 0; (e != null) && (ii < e.length); ii++) {
        err_list.add_msg(new CHValid_Msg("incomplet", e[ii].msg("Linha %L incompleta: %T - %o")));
      }
    }
    return err_list;
  }
}
