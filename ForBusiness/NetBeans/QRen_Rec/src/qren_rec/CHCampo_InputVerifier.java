package fme;

import javax.swing.InputVerifier;
import javax.swing.JComponent;




















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































class CHCampo_InputVerifier
  extends InputVerifier
{
  static CHCampo editing = null;
  

  public CHCampo_InputVerifier() {}
  
  public boolean verify(JComponent jcomp)
  {
    jcomp.setInputVerifier(null);
    

    CHCampo c = (CHCampo)jcomp.getClientProperty("handler");
    boolean ok = c.vldOnline();
    
    jcomp.setInputVerifier(this);
    if (!ok) editing = c;
    if (ok) editing = null;
    return ok;
  }
}
