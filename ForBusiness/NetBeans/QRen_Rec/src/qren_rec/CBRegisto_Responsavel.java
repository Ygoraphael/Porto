class CBRegisto_Responsavel
  extends CBRegisto
{
  Frame_Proj_1 P07;
  
  public String getPagina()
  {
    return "Proj_1";
  }
  
  CBRegisto_Responsavel()
  {
    tag = "Responsavel";
    P07 = ((Frame_Proj_1)fmeApp.Paginas.getPage("Proj_1"));
    if (P07 == null) return;
    started = true;
    
    Campos.add(new CHCampo_Text("nome", P07.getJTextField_Nome(), null));
    Campos.add(new CHCampo_Text("funcao", P07.getJTextField_Funcao(), null));
    Campos.add(new CHCampo_Text("telemovel", P07.getJTextField_Telemovel(), null, this));
    Campos.add(new CHCampo_Text("email", P07.getJTextField_Email(), CFLib.VLD_EMAIL, this));
  }
  
  void on_update(String tag) {}
  
  CHValid_Grp validar(CHValid_Grp err_list)
  {
    if (err_list == null) {
      err_list = new CHValid_Grp(this, "Responsável Técnico pelo Projeto");
    }
    extract();
    if (getByName("nome").isEmpty())
      err_list.add_msg(new CHValid_Msg("nome", "Nome - %o"));
    if (getByName("funcao").isEmpty())
      err_list.add_msg(new CHValid_Msg("funcao", "Função no beneficiário - %o"));
    if (getByName("telemovel").isEmpty())
      err_list.add_msg(new CHValid_Msg("telemovel", "Telefone - %o"));
    if (getByName("email").isEmpty()) {
      err_list.add_msg(new CHValid_Msg("email", "E-mail - %o"));
    }
    return err_list;
  }
}
