package fme;

import java.awt.Color;
import java.awt.Component;
import java.awt.Dimension;
import java.awt.Font;
import javax.swing.BorderFactory;
import javax.swing.JLabel;
import javax.swing.JTable;
import javax.swing.border.Border;
import javax.swing.table.DefaultTableCellRenderer;













































































































































































































































































































































































































































































































































































































































































































































































































































































class StdTableHeaderRenderer
  extends DefaultTableCellRenderer
{
  Color cinza = new Color(212, 208, 200);
  
  Border border = BorderFactory.createBevelBorder(0, new Color(237, 236, 233), new Color(212, 208, 200), new Color(103, 101, 98), new Color(212, 208, 200));
  

  StdTableHeaderRenderer() {}
  

  public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column)
  {
    Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
    if ((c instanceof JLabel)) {
      ((JLabel)c).setBorder(border);
      ((JLabel)c).setFont(new Font("Lucida Sans", 0, 10));
      ((JLabel)c).setBackground(cinza);
      ((JLabel)c).setHorizontalAlignment(0);
      ((JLabel)c).setPreferredSize(new Dimension(((JLabel)c).getWidth(), 22));
    }
    return c;
  }
}
