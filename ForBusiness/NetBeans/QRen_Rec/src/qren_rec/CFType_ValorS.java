package fme;

import java.text.DecimalFormat;
import java.text.DecimalFormatSymbols;
import javax.swing.JOptionPane;














































































































































































































































































































































































































































































































































































































































































class CFType_ValorS
  extends CFTypeNumber
{
  CFType_ValorS()
  {
    FMT = new DecimalFormat("#,##0.00");
  }
  
  boolean setText(String _t, boolean interact) {
    _t = _t.replace(' ', FMT.getDecimalFormatSymbols().getGroupingSeparator());
    boolean ok = super.setText(_t, interact);
    
    if (!ok) {
      return false;
    }
    ok = _lib.is_value(_t, FMT.getDecimalFormatSymbols().getDecimalSeparator(), 
      FMT.getDecimalFormatSymbols().getGroupingSeparator());
    if (!ok) {
      if (interact) {
        JOptionPane.showMessageDialog(null, "Valor estranho: " + _t + ".", 
          "Erro", 2);
      }
      return ok;
    }
    v = new Double(_lib.round(v.doubleValue()));
    
    return ok;
  }
  
  boolean setString(String s)
  {
    if (s.length() == 0) {
      v = null;
      return true;
    }
    v = new Double(Double.parseDouble(s));
    
    return true;
  }
  
  boolean acceptKey(char c) {
    if (Character.isDigit(c)) return true;
    if (c == ',') return true;
    if (c == '.') return true;
    if (c == '-') return true;
    return false;
  }
  
  boolean isnull(String _t) {
    Number x = null;
    try {
      x = FMT.parse(_t);
    }
    catch (Exception p) {
      return false;
    }
    if (x.doubleValue() == 0.0D) {
      return true;
    }
    return false;
  }
}
