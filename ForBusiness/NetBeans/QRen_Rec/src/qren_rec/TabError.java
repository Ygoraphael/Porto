class TabError
{
  int row;
  
















































































































































































































































































































  int col;
  
















































































































































































































































































































  char tipo;
  















































































































































































































































































































  String tag;
  















































































































































































































































































































  String title;
  
















































































































































































































































































































  TabError(int _row, int _col, char _tipo, String _tag, String _title)
  {
    row = _row;
    col = _col;
    tipo = _tipo;
    tag = _tag;
    title = _title;
  }
  
  String msg(String formatR, String formatN) {
    String m = "";
    if (tipo == 'R') m = formatR;
    if (tipo == 'N') m = formatN;
    m = m.replaceAll("%L", Integer.toString(row + 1));
    title = title.replaceAll("\n", " ");
    title = title.replaceAll("<br>", " ");
    title = title.replaceAll("<html>", "");
    title = title.replaceAll("</html>", "");
    title = title.replaceAll("<div align='center'>", "");
    title = title.replaceAll("</div>", "");
    m = m.replaceAll("%T", title);
    return m;
  }
  
  String msg(String formatR) {
    return msg(formatR, "");
  }
}
