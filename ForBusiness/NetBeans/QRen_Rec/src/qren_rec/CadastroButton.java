package fme;

import java.awt.Dimension;
import java.awt.Insets;
import java.awt.event.MouseAdapter;
import java.awt.event.MouseEvent;
import javax.swing.ImageIcon;
import javax.swing.JButton;































































































































































































































































































class CadastroButton
  extends JButton
{
  private static final long serialVersionUID = 1L;
  
  CadastroButton(int y) { this(35, 37, y); }
  
  CadastroButton(int w, int h, int y) {
    setIcon(new ImageIcon(fmeFrame.class.getResource("pas_icon.gif")));
    setToolTipText("Importar dados da PAS");
    setMaximumSize(new Dimension(w, h));
    setMinimumSize(new Dimension(w, h));
    setMargin(new Insets(0, 0, 0, 0));
    setBounds(525, y, 42, 30);
    setBackground(fmeComum.toolbarButtonBg);
    setBorder(fmeComum.toolbarButtonBorder);
    

    addMouseListener(new MouseAdapter() {
      public void mousePressed(MouseEvent me) {
        if ((!CBData.T.equals("")) || (!fmeComum.ON)) return;
        CadastroConnection cadastro = new CadastroConnection();
        cadastro.showDialog(fmeApp.jScrollPane);
      }
    });
  }
}
