class MultiLineHeaderRenderer
  extends JList
  implements TableCellRenderer
{
  public MultiLineHeaderRenderer()
  {
    Color cinza = new Color(212, 208, 200);
    Border border = BorderFactory.createBevelBorder(0, new Color(237, 236, 233), new Color(212, 208, 200), new Color(103, 101, 98), new Color(212, 208, 200));
    setOpaque(true);
    setBackground(cinza);
    setBorder(border);
    ListCellRenderer renderer = getCellRenderer();
    ((JLabel)renderer).setHorizontalAlignment(0);
    ((JLabel)renderer).setPreferredSize(new Dimension(((JLabel)renderer).getWidth(), 18));
    setCellRenderer(renderer);
  }
  
  public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column)
  {
    setFont(fmeComum.letra_pequena);
    String str = value == null ? "" : value.toString();
    BufferedReader br = new BufferedReader(new StringReader(str));
    
    Vector v = new Vector();
    try { String line;
      while ((line = br.readLine()) != null) { String line;
        v.addElement(line);
      }
    } catch (IOException ex) {
      ex.printStackTrace();
    }
    setListData(v);
    
    return this;
  }
}
