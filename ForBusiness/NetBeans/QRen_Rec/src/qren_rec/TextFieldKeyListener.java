class TextFieldKeyListener
  extends KeyAdapter
{
  JFormattedTextField j;
  
  TextFieldKeyListener(JFormattedTextField _j)
  {
    j = _j;
  }
  
  public void keyTyped(KeyEvent e)
  {
    CHCampo_Text h = (CHCampo_Text)j.getClientProperty("handler");
    CFType vld = vld;
    
    char c = e.getKeyChar();
    if (c == '\b') {
      return;
    }
    if (c == '') {
      return;
    }
    if (!vld.acceptKey(c)) {
      e.consume();
    }
  }
}
