package fme;

import java.util.Vector;













































































































































































































































































































































































































































class CTabelaF1
  extends CTabela
{
  CTabela base;
  
  CTabelaF1(CTabela _base)
  {
    base = _base;
    name = (base.name + "F1");
    titles = base.titles;
    data_table = base.data_table;
    cCol = base.cCol;
    dCol = base.dCol;
  }
  
  public void clear_filter() {
    data_table = base.data_table;
  }
  
  public void clear_combo() {
    data_table = new Vector();
  }
  
  public boolean set_filter(int nfCol, String key) {
    data_table = new Vector();
    
    for (int i = 0; i < base.data_table.size(); i++) {
      String[] item = (String[])base.data_table.elementAt(i);
      if (item[nfCol].equals(key)) data_table.addElement(item);
    }
    return true;
  }
}
