package fme;

import java.util.Vector;
import javax.swing.table.AbstractTableModel;




































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































class NoFixedAbstractTableModel
  extends AbstractTableModel
{
  Vector data;
  Object[] column;
  int i;
  
  NoFixedAbstractTableModel(Vector data, Object[] column, int i)
  {
    this.data = data;
    this.column = column;
    this.i = i;
  }
  
  public int getColumnCount() { return column.length - i; }
  
  public int getRowCount() {
    return data.size();
  }
  
  public String getColumnName(int col) { return (String)column[(col + i)]; }
  
  public Object getValueAt(int row, int col) {
    return ((String[])data.elementAt(row))[(col + i)];
  }
  
  public void setValueAt(Object obj, int row, int col) { ((String[])data.elementAt(row))[(col + i)] = ((String)obj); }
  
  public boolean CellEditable(int row, int col)
  {
    return true;
  }
}
