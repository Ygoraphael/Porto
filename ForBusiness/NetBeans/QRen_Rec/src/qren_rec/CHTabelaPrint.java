class CHTabelaPrint
  extends CHTabela
{
  public int print_rows;
  public int row_offset = 0;
  
  CHTabelaPrint(CHTabela org) {
    j = j;
    width = width;
    

    d = d;
    



    cdx = cdx;
    
    print_rows = org.getRowCount();
  }
  
  public int getRowCount()
  {
    return print_rows;
  }
  
  public Object getValueAt(int i, int j) {
    i += row_offset;
    if (i >= d.dados.size()) return "";
    return super.getValueAt(i, j);
  }
}
