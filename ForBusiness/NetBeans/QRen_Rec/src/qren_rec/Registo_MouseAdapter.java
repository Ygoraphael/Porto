package fme;

import java.awt.Cursor;
import java.awt.event.MouseEvent;
import java.awt.event.MouseListener;
import javax.swing.JOptionPane;























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































class Registo_MouseAdapter
  implements MouseListener
{
  ToolBar t;
  
  Registo_MouseAdapter(ToolBar _t)
  {
    t = _t;
  }
  

  public void mousePressed(MouseEvent e) {}
  
  public void mouseReleased(MouseEvent e) {}
  
  public void mouseClicked(MouseEvent e)
  {
    if ((fmeApp.in_pas) || (!CBData.T.equals("")) || (!fmeComum.ON)) return;
    if (CBData.reg_C.equals("")) {
      JOptionPane.showMessageDialog(null, "Antes do Registo é necessário Guardar a sua candidatura.\nP.f., siga os passos.", 
        "Aviso", 
        2);
      int s = -1;
      while (s == -1) {
        s = fmeApp.comum.guardar(true, false, false, false, false);
      }
      if (s == 0) return;
    }
    CHRegisto.ligacao();
  }
  
  public void mouseEntered(MouseEvent e) { if ((fmeApp.in_pas) || (!CBData.T.equals("")) || (!fmeComum.ON)) return;
    t.setCursor(Cursor.getPredefinedCursor(12));
  }
  
  public void mouseExited(MouseEvent e) { t.setCursor(Cursor.getPredefinedCursor(0)); }
}
