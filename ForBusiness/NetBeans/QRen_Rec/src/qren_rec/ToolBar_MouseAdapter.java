package fme;

import java.awt.Color;
import java.awt.event.MouseEvent;
import java.awt.event.MouseListener;
import javax.swing.JButton;
import javax.swing.border.LineBorder;






















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































class ToolBar_MouseAdapter
  implements MouseListener
{
  JButton adaptee;
  JButton adaptee2;
  
  ToolBar_MouseAdapter(JButton adaptee) { this.adaptee = adaptee; }
  
  ToolBar_MouseAdapter(JButton adaptee, JButton adaptee2) {
    this.adaptee = adaptee;
    this.adaptee2 = adaptee2;
  }
  
  public void mouseClicked(MouseEvent e) {
    adaptee.setBorder(null);
    adaptee.setBackground(null);
  }
  
  public void mousePressed(MouseEvent e) {}
  
  public void mouseReleased(MouseEvent e) {}
  
  public void mouseEntered(MouseEvent e) { if (adaptee.isEnabled()) {
      adaptee.setBorder(new LineBorder(new Color(180, 180, 180)));
      adaptee.setBackground(fmeComum.colorBorder);
      if (adaptee2 != null) {
        adaptee2.setBorder(new LineBorder(new Color(180, 180, 180)));
        adaptee2.setBackground(fmeComum.colorBorder);
      }
    }
  }
  
  public void mouseExited(MouseEvent e) { if (adaptee.isEnabled()) {
      adaptee.setBorder(null);
      adaptee.setBackground(null);
      if (adaptee2 != null) {
        adaptee2.setBorder(null);
        adaptee2.setBackground(null);
      }
    }
  }
}
