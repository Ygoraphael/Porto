package fme;

import java.awt.event.ComponentAdapter;
import java.awt.event.ComponentEvent;

































































































































































































































































































































































































































































































class fmeApplet_this_componentAdapter
  extends ComponentAdapter
{
  fmeApplet adaptee;
  
  fmeApplet_this_componentAdapter(fmeApplet adaptee)
  {
    this.adaptee = adaptee;
  }
  
  public void componentResized(ComponentEvent e) { adaptee.this_componentResized(e); }
}
