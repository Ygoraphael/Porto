package fme;

import java.awt.event.KeyAdapter;
import java.awt.event.KeyEvent;






































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































class TypeKeyListener
  extends KeyAdapter
{
  CFType t;
  
  TypeKeyListener(CFType _t)
  {
    t = _t;
  }
  
  public void keyTyped(KeyEvent e) {
    if (t == null) {
      return;
    }
    char c = e.getKeyChar();
    if (c == '\b') {
      return;
    }
    if (c == '') {
      return;
    }
    if (!t.acceptKey(c)) {
      e.consume();
    }
  }
}
