class CEECs
  extends CTabela
{
  public CEECs(String nome, String filename)
  {
    _init_tab(nome, filename);
    cCol = 0;
    dCol = 1;
  }
  
  String getRegiao(String code) {
    int n = getIndexFromCode(code);
    return getColFromIndex(n + 1, 2);
  }
}
