package fme;


























































class CHTabColModel
{
  String col_tag;
  
























































  boolean xml_enabled;
  
























































  boolean editable;
  
























































  boolean visible;
  
























































  String col_name;
  
























































  CFType vld;
  
























































  int t_col;
  
























































  String autocalc;
  
























































  boolean disable_row_calc;
  
























































  boolean is_check;
  

























































  CHTabColModel(String _tag, String _name, boolean _xml, boolean _editable, boolean _visible, CFType _v)
  {
    col_tag = _tag;
    col_name = ("<html><div align='center'>" + _name + "</div></html>");
    xml_enabled = _xml;
    editable = _editable;
    visible = _visible;
    vld = _v;
    autocalc = null;
    disable_row_calc = false;
    is_check = false;
  }
}
